public class GOCommonTags {

    private final String ID = "id";
    private final String NAME = "name";

    /**
     * Get the ID tag
     *
     * @return ID
     */
    public String getID() {
        return ID;
    }

    /**
     * Get the NAME tag
     *
     * @return NAME
     */
    public String getNAME() {
        return NAME;
    }
}
